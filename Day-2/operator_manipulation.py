#direct calculation
print(8/3)
# modify the float with int to round
print(int(8/3))
# rounding using round()
print(round(8/3))
# rounding using round() also specifying the float point
print(round(8/3, 2))
#directly to integer
print(8 // 2)

# dividing with variable
result = 8/2
# info can still be divided without add the main number again
result /= 2
print(result)

#to manipulate a number
score = 0

# working on the score
score += 1
score -= 1
score /= 1
score *= 1

print(score)
